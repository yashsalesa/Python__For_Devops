# Integer variables
num1 = 10
num2 = 3

# Integer Division
result1 = num1 // num2  #Here double slash is to get floored value (integer)
print("Integer Division:", result1)

# Modulus (Remainder)
result2 = num1 % num2
print("Modulus (Remainder):", result2)

# Absolute Value (To convert negative value to a positive value)
result3 = abs(-7)
print("Absolute Value:", result3)